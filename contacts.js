const rand = (max, min = 0) =>
  Math.floor(Math.random() * (max - min + 1)) + min;

const generateName = () =>
  `${firstNames[rand(firstNames.length - 1)]} ${
    lastNames[rand(lastNames.length - 1)]
  }`;

const generatePhone = () =>
  `(+995)-${rand(599, 500)}-${String(rand(99)).padStart(3, "0")}-${String(
    rand(99)
  ).padStart(3, "0")}`;

const createContact = () => ({
  id: Math.random().toString(36).substring(2, 9),
  name: generateName(),
  phone: generatePhone(),
});

export const compareNames = (contact1, contact2) =>
    contact1.name.localeCompare(contact2.name);

const contacts = Array.from({ length: NUM_CONTACTS }, createContact);

